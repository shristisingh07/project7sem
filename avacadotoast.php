    <h2>Ingredients</h2>
    <ul>
        <li>2 slices of whole grain bread</li>
        <li>1 ripe avocado</li>
        <li>2 eggs</li>
        <li>1/2 lemon, juiced</li>
        <li>Salt and pepper to taste</li>
        <li>Red pepper flakes (optional)</li>
        <li>Fresh cilantro or parsley for garnish (optional)</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li>Toast the bread slices until golden and crisp.</li>
        <li>While the bread is toasting, cut the avocado in half, remove the pit, and scoop the flesh into a bowl.</li>
        <li>Add lemon juice, salt, and pepper to the avocado and mash with a fork until creamy but still slightly chunky.</li>
        <li>In a non-stick pan, cook the eggs to your preference (fried or poached work best).</li>
        <li>Spread the mashed avocado evenly on the toasted bread slices.</li>
        <li>Top each toast with a cooked egg.</li>
        <li>Season with additional salt, pepper, and red pepper flakes if desired.</li>
        <li>Garnish with fresh herbs and serve immediately.</li>
    </ol>
    
    <h2>Notes</h2>
    <p>For extra flavor, you can add a sprinkle of feta cheese or a drizzle of hot sauce. This recipe is easily customizable with toppings like cherry tomatoes, microgreens, or smoked salmon.</p>
    
