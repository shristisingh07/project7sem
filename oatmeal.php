<body>
    <h1>Oatmeal with Nuts and Fruits</h1>
    
    <div class="recipe-info">
        <p>A delicious and nutritious breakfast packed with fiber, protein, and natural sweetness from fresh fruits.</p>
    </div>
    
    <div class="ingredients">
        <h2>Ingredients</h2>
        <ul>
            <li>1/2 cup rolled oats</li>
            <li>1 cup water or milk (dairy or plant-based)</li>
            <li>1/4 teaspoon cinnamon</li>
            <li>Pinch of salt</li>
            <li>1 tablespoon honey or maple syrup (optional)</li>
            <li>1/4 cup mixed nuts (almonds, walnuts, pecans), chopped</li>
            <li>1/4 cup fresh fruits (berries, banana, apple), sliced</li>
            <li>1 teaspoon chia seeds or flaxseeds (optional)</li>
        </ul>
    </div>
    
    <div class="instructions">
        <h2>Instructions</h2>
        <ol>
            <li>In a small saucepan, bring water or milk to a gentle boil.</li>
            <li>Add the oats, cinnamon, and salt. Reduce heat to medium-low.</li>
            <li>Cook for about 5 minutes, stirring occasionally, until the oats are soft and have absorbed most of the liquid.</li>
            <li>Remove from heat and let it sit for 1-2 minutes to thicken.</li>
            <li>Transfer to a bowl and drizzle with honey or maple syrup if desired.</li>
            <li>Top with chopped nuts and fresh fruits.</li>
            <li>Sprinkle with chia seeds or flaxseeds for extra nutrition.</li>
            <li>Serve warm and enjoy!</li>
        </ol>
    </div>
    
    <div class="notes">
        <p><strong>Note:</strong> You can customize this recipe with your favorite nuts and seasonal fruits. For a quicker version, use quick oats which cook in about 1-2 minutes.</p>
