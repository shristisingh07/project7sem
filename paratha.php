    <h1>Stuffed Paratha with Spinach & Paneer</h1>
    
    <div class="recipe-info">
        <p>A delicious and nutritious stuffed Indian flatbread filled with spinach and paneer (Indian cottage cheese). Perfect for breakfast or lunch!</p>
    </div>
    
    <div class="time-servings">
        <div>
            <strong>Prep Time</strong><br>
            20 mins
        </div>
        <div>
            <strong>Cook Time</strong><br>
            25 mins
        </div>
        <div>
            <strong>Servings</strong><br>
            4 people
        </div>
    </div>
    
    <h2>Ingredients</h2>
    <h3>For the Dough:</h3>
    <ul>
        <li>2 cups whole wheat flour</li>
        <li>1/2 teaspoon salt</li>
        <li>1 tablespoon oil</li>
        <li>Water as needed</li>
    </ul>
    
    <h3>For the Filling:</h3>
    <ul>
        <li>1 cup paneer, crumbled</li>
        <li>1 cup spinach, finely chopped</li>
        <li>1 small onion, finely chopped</li>
        <li>1 green chili, finely chopped</li>
        <li>1/2 teaspoon cumin powder</li>
        <li>1/2 teaspoon coriander powder</li>
        <li>1/2 teaspoon garam masala</li>
        <li>1/2 teaspoon red chili powder</li>
        <li>Salt to taste</li>
        <li>1 tablespoon oil</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li><strong>Prepare the dough:</strong> In a large bowl, mix flour, salt, and oil. Gradually add water and knead to make a soft dough. Cover and let it rest for 15-20 minutes.</li>
        <li><strong>Make the filling:</strong> Heat oil in a pan. Add onions and green chili, saut√© until onions turn translucent.</li>
        <li>Add spinach and cook until it wilts. Then add all the spices and mix well.</li>
        <li>Add crumbled paneer and mix everything together. Cook for 2-3 minutes. Remove from heat and let it cool.</li>
        <li><strong>Assemble the parathas:</strong> Divide the dough into equal portions (about lemon-sized balls). Take one portion, flatten it slightly, and roll into a small circle (about 4 inches diameter).</li>
        <li>Place a spoonful of filling in the center. Bring the edges together to seal the filling inside, then gently flatten.</li>
        <li>Dust with flour and carefully roll out into a circle (about 6-7 inches diameter), being careful not to let the filling come out.</li>
        <li><strong>Cook the parathas:</strong> Heat a tawa or griddle over medium heat. Place the rolled paratha on it.</li>
        <li>Cook for about 30 seconds, then flip. Spread some oil/ghee on the cooked side, then flip again and spread oil on the other side.</li>
        <li>Cook until both sides are golden brown with some crispy spots.</li>
        <li>Repeat with remaining dough and filling.</li>
    </ol>
    
    <h2>Serving Suggestions</h2>
    <p>Serve hot with yogurt, pickle, or your favorite chutney. These parathas also taste great with a cup of hot tea!</p>
    
    <div class="tips">
        <h3>Tips:</h3>
        <ul>
            <li>Make sure the filling is completely cooled before stuffing to prevent the dough from becoming sticky.</li>
            <li>If the dough becomes dry while rolling, sprinkle some water and knead again.</li>
            <li>You can add other vegetables like grated carrots or boiled potatoes to the filling for variation.</li>
        </ul>
