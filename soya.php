    <h1>Soya Chunk Curry with Brown Rice</h1>
    
    <div class="recipe-info">
        <p>A healthy and protein-packed vegetarian meal that's both nutritious and delicious. Soya chunks cooked in a flavorful curry served with fiber-rich brown rice.</p>
        
        <div class="time-servings">
            <div class="time">Prep Time: 15 mins</div>
            <div class="time">Cook Time: 30 mins</div>
            <div class="servings">Servings: 4</div>
        </div>
    </div>
    
    <h2>Ingredients</h2>
    <h3>For Soya Chunk Curry:</h3>
    <ul>
        <li>1 cup soya chunks</li>
        <li>2 tablespoons oil</li>
        <li>1 teaspoon cumin seeds</li>
        <li>1 large onion, finely chopped</li>
        <li>2 tomatoes, pureed</li>
        <li>1 tablespoon ginger-garlic paste</li>
        <li>1 teaspoon turmeric powder</li>
        <li>1 teaspoon red chili powder</li>
        <li>1 teaspoon coriander powder</li>
        <li>1 teaspoon garam masala</li>
        <li>Salt to taste</li>
        <li>2 cups water</li>
        <li>Fresh coriander leaves for garnish</li>
    </ul>
    
    <h3>For Brown Rice:</h3>
    <ul>
        <li>1 cup brown rice</li>
        <li>2 cups water</li>
        <li>1/2 teaspoon salt</li>
        <li>1 teaspoon ghee or oil (optional)</li>
    </ul>
    
    <h2>Instructions</h2>
    <h3>For Brown Rice:</h3>
    <ol>
        <li>Rinse the brown rice thoroughly under running water.</li>
        <li>In a pot, bring 2 cups of water to boil. Add salt and ghee/oil if using.</li>
        <li>Add the rinsed brown rice, reduce heat to low, cover and simmer for 30-35 minutes until water is absorbed and rice is tender.</li>
        <li>Remove from heat and let it sit covered for 5 minutes before fluffing with a fork.</li>
    </ol>
    
    <h3>For Soya Chunk Curry:</h3>
    <ol>
        <li>Soak soya chunks in warm water for 15 minutes. Squeeze out excess water and rinse 2-3 times.</li>
        <li>Heat oil in a pan. Add cumin seeds and let them splutter.</li>
        <li>Add chopped onions and sauté until golden brown.</li>
        <li>Add ginger-garlic paste and sauté for 1 minute until raw smell disappears.</li>
        <li>Add tomato puree and cook until oil separates from the mixture.</li>
        <li>Add all the spices (turmeric, red chili, coriander, garam masala) and salt. Cook for 2 minutes.</li>
        <li>Add soya chunks and mix well to coat them with the masala.</li>
        <li>Add water, cover and simmer for 10-15 minutes on medium heat.</li>
        <li>Garnish with fresh coriander leaves before serving.</li>
    </ol>
    
    <h2>Serving Suggestion</h2>
    <p>Serve the hot soya chunk curry with freshly prepared brown rice. You can accompany it with a side of raita or salad for a complete meal.</p>
    
