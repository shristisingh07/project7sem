    <h2>Ingredients</h2>
    <ul>
        <li>4 large capsicums (bell peppers)</li>
        <li>1 cup cooked foxtail millet (or any millet of choice)</li>
        <li>200g paneer, crumbled</li>
        <li>1 medium onion, finely chopped</li>
        <li>2 tomatoes, finely chopped</li>
        <li>1 tsp ginger-garlic paste</li>
        <li>1 tsp cumin seeds</li>
        <li>1 tsp garam masala</li>
        <li>1 tsp red chili powder</li>
        <li>1/2 tsp turmeric powder</li>
        <li>1 tsp coriander powder</li>
        <li>2 tbsp oil</li>
        <li>Salt to taste</li>
        <li>Fresh coriander leaves for garnish</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li>Wash the capsicums and cut off the tops. Remove the seeds and membranes carefully without breaking the capsicums.</li>
        <li>Heat 1 tbsp oil in a pan. Add cumin seeds and let them splutter.</li>
        <li>Add chopped onions and sauté until translucent. Add ginger-garlic paste and sauté for another minute.</li>
        <li>Add chopped tomatoes and cook until they turn soft and mushy.</li>
        <li>Add all the dry spices (turmeric, red chili powder, coriander powder, garam masala) and salt. Mix well.</li>
        <li>Add crumbled paneer and cooked millet. Mix everything well and cook for 2-3 minutes. Adjust seasoning if needed.</li>
        <li>Stuff this mixture into the prepared capsicums generously.</li>
        <li>Heat the remaining oil in a pan or pressure cooker. Place the stuffed capsicums carefully.</li>
        <li>Cover and cook on low flame for about 15-20 minutes, rotating them occasionally for even cooking.</li>
        <li>Once the capsicums are cooked (they should be tender but still hold their shape), remove from heat.</li>
        <li>Garnish with fresh coriander leaves and serve hot.</li>
    </ol>
    
    <div class="tips">
        <strong>Tips:</strong> You can add grated cheese on top before the final cooking for a cheesy version. For a vegan option, replace paneer with tofu.
