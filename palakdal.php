    <h1>Palak Dal with Egg Recipe</h1>
    
    <div class="recipe-info">
        <p><strong>Prep Time:</strong> 15 minutes | <strong>Cook Time:</strong> 30 minutes | <strong>Total Time:</strong> 45 minutes</p>
        <p><strong>Servings:</strong> 4 | <strong>Difficulty:</strong> Medium</p>
    </div>
    
    <h2>Ingredients</h2>
    <ul>
        <li>1 cup toor dal (split pigeon peas)</li>
        <li>2 cups fresh spinach (palak), chopped</li>
        <li>4 eggs</li>
        <li>1 onion, finely chopped</li>
        <li>2 tomatoes, chopped</li>
        <li>3-4 garlic cloves, minced</li>
        <li>1-inch ginger, grated</li>
        <li>2 green chilies, slit</li>
        <li>1 tsp turmeric powder</li>
        <li>1 tsp red chili powder</li>
        <li>1 tsp cumin seeds</li>
        <li>1 tsp garam masala</li>
        <li>1 tbsp oil or ghee</li>
        <li>Salt to taste</li>
        <li>Fresh coriander leaves for garnish</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li>Wash the toor dal thoroughly and pressure cook with 2.5 cups water, turmeric powder, and salt for 3-4 whistles until soft.</li>
        <li>Heat oil or ghee in a pan. Add cumin seeds and let them splutter.</li>
        <li>Add chopped onions and sauté until golden brown. Then add ginger, garlic, and green chilies. Sauté for 1 minute.</li>
        <li>Add chopped tomatoes and cook until they turn mushy. Add red chili powder and salt.</li>
        <li>Add chopped spinach and cook for 2-3 minutes until wilted.</li>
        <li>Mash the cooked dal slightly and add it to the spinach mixture. Mix well and simmer for 5 minutes.</li>
        <li>Make four small wells in the dal mixture and carefully break an egg into each well.</li>
        <li>Cover the pan and cook on low heat for 5-7 minutes until the eggs are cooked to your liking.</li>
        <li>Sprinkle garam masala and garnish with fresh coriander leaves.</li>
        <li>Serve hot with steamed rice or roti.</li>
    </ol>
    
    <div class="note">
        <p><strong>Note:</strong> You can adjust the consistency of the dal by adding water as needed. For runny eggs, cook for less time.</p>
