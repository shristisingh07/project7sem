    <h1>Mixed Lentil Dosa with Coconut Chutney</h1>
    
    <div class="section">
        <h2>Ingredients for Mixed Lentil Dosa</h2>
        <ul>
            <li>½ cup rice</li>
            <li>½ cup urad dal (split black gram)</li>
            <li>¼ cup chana dal (split chickpeas)</li>
            <li>¼ cup moong dal (split mung beans)</li>
            <li>¼ cup toor dal (pigeon peas)</li>
            <li>1 tbsp fenugreek seeds</li>
            <li>Salt to taste</li>
            <li>Water as needed</li>
            <li>Oil for cooking</li>
        </ul>
    </div>
    
    <div class="section">
        <h2>Instructions for Dosa</h2>
        <ol>
            <li>Rinse all the lentils and rice together in water 2-3 times.</li>
            <li>Soak them along with fenugreek seeds in enough water for 6-8 hours.</li>
            <li>Drain the water and grind everything to a smooth batter using fresh water.</li>
            <li>The batter should be of pouring consistency but not too thin.</li>
            <li>Add salt and mix well. Let it ferment overnight or for 8-10 hours.</li>
            <li>Heat a non-stick tawa or griddle on medium heat.</li>
            <li>Pour a ladleful of batter and spread it in circular motion to make thin dosa.</li>
            <li>Drizzle some oil around the edges and cook until golden brown.</li>
            <li>Flip and cook the other side for a minute.</li>
            <li>Serve hot with coconut chutney.</li>
        </ol>
    </div>
    
    <div class="section">
        <h2>Ingredients for Coconut Chutney</h2>
        <ul>
            <li>1 cup grated fresh coconut</li>
            <li>2-3 green chilies</li>
            <li>1 inch ginger</li>
            <li>½ cup roasted chana dal</li>
            <li>½ tsp salt</li>
            <li>Water as needed</li>
            <li>For tempering: 1 tsp oil, ½ tsp mustard seeds, few curry leaves, pinch of asafoetida</li>
        </ul>
    </div>
    
    <div class="section">
        <h2>Instructions for Coconut Chutney</h2>
        <ol>
            <li>Grind coconut, green chilies, ginger, roasted chana dal and salt with little water to smooth paste.</li>
            <li>Transfer to serving bowl.</li>
            <li>Heat oil in small pan, add mustard seeds and let them splutter.</li>
            <li>Add curry leaves and asafoetida, pour this tempering over chutney.</li>
            <li>Mix well before serving.</li>
        </ol>
    </div>
    
    <div class="section">
        <p class="notes"><strong>Notes:</strong> The fermentation time may vary depending on weather. In cold climates, it may take longer. You can keep the batter in warm place to speed up fermentation.</p>
