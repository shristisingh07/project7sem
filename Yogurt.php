    <h1>Quinoa & Vegetables Bowl with Yogurt Dressing</h1>
    
    <div class="recipe-info">
        <p><strong>Prep Time:</strong> 15 minutes | <strong>Cook Time:</strong> 20 minutes | <strong>Total Time:</strong> 35 minutes | <strong>Servings:</strong> 2</p>
    </div>
    
    <h2>Ingredients</h2>
    <h3>For the Bowl:</h3>
    <ul>
        <li>1 cup quinoa, rinsed</li>
        <li>2 cups water or vegetable broth</li>
        <li>1 cup cherry tomatoes, halved</li>
        <li>1 cucumber, diced</li>
        <li>1 bell pepper (any color), diced</li>
        <li>1 avocado, sliced</li>
        <li>1 cup baby spinach or kale</li>
        <li>¼ cup red onion, finely chopped</li>
    </ul>
    
    <h3>For the Yogurt Dressing:</h3>
    <ul>
        <li>½ cup plain Greek yogurt</li>
        <li>2 tablespoons lemon juice</li>
        <li>1 tablespoon olive oil</li>
        <li>1 garlic clove, minced</li>
        <li>1 teaspoon honey or maple syrup</li>
        <li>½ teaspoon ground cumin</li>
        <li>Salt and pepper to taste</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li><strong>Cook the quinoa:</strong> In a medium saucepan, bring the water or broth to a boil. Add quinoa, reduce heat to low, cover and simmer for about 15 minutes or until liquid is absorbed. Remove from heat and let stand for 5 minutes, then fluff with a fork.</li>
        <li><strong>Prepare vegetables:</strong> While quinoa cooks, wash and chop all vegetables as indicated in the ingredients list.</li>
        <li><strong>Make the dressing:</strong> In a small bowl, whisk together all dressing ingredients until smooth. Adjust seasoning to taste.</li>
        <li><strong>Assemble bowls:</strong> Divide cooked quinoa between two bowls. Arrange vegetables on top of the quinoa in sections or mixed together as preferred.</li>
        <li><strong>Serve:</strong> Drizzle with yogurt dressing just before serving. You can also serve extra dressing on the side.</li>
    </ol>
    
    <h2>Notes</h2>
    <div class="notes">
        <p>• For extra protein, add chickpeas, grilled chicken, or tofu.</p>
        <p>• This bowl keeps well in the refrigerator for up to 2 days (store dressing separately).</p>
        <p>• For a vegan version, use dairy-free yogurt and maple syrup instead of honey.</p>
