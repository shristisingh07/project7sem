    <h2>Ingredients</h2>
    <ul>
        <li>1 cup sprouted moong beans (green gram)</li>
        <li>1 small onion, finely chopped</li>
        <li>1 small tomato, finely chopped</li>
        <li>1 small cucumber, finely chopped</li>
        <li>1 green chili, finely chopped (optional)</li>
        <li>1/4 cup fresh coriander leaves, chopped</li>
        <li>1/2 tsp chaat masala</li>
        <li>1/2 tsp roasted cumin powder</li>
        <li>1/4 tsp black salt</li>
        <li>1 tbsp lemon juice</li>
        <li>Salt to taste</li>
        <li>Sev (thin gram flour noodles) for garnish (optional)</li>
    </ul>
    
    <h2>Instructions</h2>
    <ol>
        <li>Rinse the sprouted moong beans thoroughly under running water.</li>
        <li>If you prefer softer sprouts, you can steam them for 3-4 minutes or blanch in hot water for 2 minutes. Drain well.</li>
        <li>In a large mixing bowl, combine the sprouts, chopped onion, tomato, cucumber, and green chili.</li>
        <li>Add chaat masala, roasted cumin powder, black salt, regular salt, and lemon juice.</li>
        <li>Mix everything well. Taste and adjust seasoning if needed.</li>
        <li>Garnish with fresh coriander leaves and sev (if using).</li>
        <li>Serve immediately for the best taste and texture.</li>
    </ol>
    
    <div class="tips">
        <h2>Tips</h2>
        <p>You can add boiled potatoes or pomegranate seeds for extra flavor and texture. For protein boost, add some boiled chickpeas. Adjust the spices according to your taste preference.</p>
