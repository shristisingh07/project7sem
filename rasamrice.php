        <h2>Ingredients</h2>
        <h3>For Rasam:</h3>
        <ul>
            <li>1/2 cup toor dal (split pigeon peas)</li>
            <li>2 medium tomatoes, chopped</li>
            <li>1 small lemon-sized tamarind</li>
            <li>1 tsp rasam powder</li>
            <li>1/2 tsp turmeric powder</li>
            <li>1 tsp mustard seeds</li>
            <li>1 tsp cumin seeds</li>
            <li>2-3 dried red chilies</li>
            <li>10-12 curry leaves</li>
            <li>2 tbsp chopped coriander leaves</li>
            <li>1/4 tsp asafoetida (hing)</li>
            <li>1/2 tsp jaggery (optional)</li>
            <li>Salt to taste</li>
            <li>2 tsp oil</li>
            <li>4 cups water</li>
        </ul>
        
        <h3>For Rice:</h3>
        <ul>
            <li>1 cup rice (preferably short grain)</li>
            <li>2 cups water</li>
            <li>1 tsp ghee</li>
            <li>1/2 tsp salt</li>
        </ul>
        
        <h2>Instructions</h2>
        <ol>
            <li><strong>Cook the dal:</strong> Pressure cook toor dal with 1 cup water and 1/4 tsp turmeric for 3-4 whistles. Mash well and set aside.</li>
            <li><strong>Prepare tamarind extract:</strong> Soak tamarind in 1/2 cup warm water for 10 mins, extract juice and discard pulp.</li>
            <li><strong>Make rasam base:</strong> In a pot, add chopped tomatoes, tamarind extract, 3 cups water, turmeric, rasam powder, salt and jaggery. Boil for 10 mins until raw smell disappears.</li>
            <li><strong>Add dal:</strong> Add mashed dal to the rasam base and mix well. Simmer for 5 mins.</li>
            <li><strong>Tempering:</strong> Heat oil in a small pan. Add mustard seeds, cumin seeds, red chilies, curry leaves and asafoetida. When mustard pops, pour this tempering into the rasam.</li>
            <li><strong>Finish:</strong> Add coriander leaves and turn off heat. Don't boil after adding tempering.</li>
            <li><strong>Cook rice:</strong> Wash rice and cook with 2 cups water, ghee and salt in a pressure cooker for 2 whistles or in a pot until done.</li>
            <li><strong>Serve:</strong> Mix rasam with rice or serve separately. Garnish with more coriander leaves.</li>
        </ol>
        
        <h2>Tips</h2>
        <ul>
            <li>Adjust water quantity based on desired consistency.</li>
            <li>You can add 1/4 tsp black pepper powder for extra spice.</li>
            <li>For variation, add 1/4 cup chopped carrots or beans while cooking tomatoes.</li>
        </ul>
        
